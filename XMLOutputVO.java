public class XMLOutputVO {
	
	String systemName = "";
	
	String categoryName = "";
	
	String btnName = "";
	
	String training = "";
	
	
	public XMLOutputVO(String systemName, String categoryName, String btnName, String training) {
		
		this.systemName = systemName;
		
		this.categoryName = categoryName;
		
		this.btnName = btnName;
		
		this.training = training;
		
	}
}
